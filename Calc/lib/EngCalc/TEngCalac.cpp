#include "TEngCalac.h"

#include <cmath>

TEngCalac::TEngCalac() {}

TEngCalac::~TEngCalac() {}

double TEngCalac::sum(const double& a, const double& b) { return a + b; }

double TEngCalac::sub(const double& a, const double& b) { return a - b; }

int TEngCalac::squareR(const int& a) { return std::sqrt(a); }